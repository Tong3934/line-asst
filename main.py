def extract_phone_from_response(text: str) -> Optional[str]:
    """
    ‡∏î‡∏∂‡∏á‡πÄ‡∏ö‡∏≠‡∏£‡πå‡πÇ‡∏ó‡∏£‡∏à‡∏≤‡∏Å‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° AI
    ‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö:
    - ‡πÇ‡∏ó‡∏£ 1557
    - ‡πÇ‡∏ó‡∏£ 02-123-4567
    - ‡πÄ‡∏ö‡∏≠‡∏£‡πå: 098-765-4321
    - ‡πÇ‡∏ó‡∏£‡∏®‡∏±‡∏û‡∏ó‡πå: 1234567890

    Returns:
        ‡πÄ‡∏ö‡∏≠‡∏£‡πå‡πÇ‡∏ó‡∏£‡∏ó‡∏µ‡πà‡πÄ‡∏≠‡∏≤ - ‡πÅ‡∏•‡∏∞‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏≠‡∏≠‡∏Å ‡∏´‡∏£‡∏∑‡∏≠ None ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏û‡∏ö
    """
    patterns = [
        r'‡πÇ‡∏ó‡∏£\s*(\d{4})',                          # ‡πÇ‡∏ó‡∏£ 1557
        r'‡πÇ‡∏ó‡∏£\s*(\d{2,3}[-\s]?\d{3}[-\s]?\d{4})',  # ‡πÇ‡∏ó‡∏£ 02-123-4567
        r'‡πÄ‡∏ö‡∏≠‡∏£‡πå[:\s]*(\d{2,3}[-\s]?\d{3}[-\s]?\d{4})',  # ‡πÄ‡∏ö‡∏≠‡∏£‡πå: 098-765-4321
        r'‡πÇ‡∏ó‡∏£‡∏®‡∏±‡∏û‡∏ó‡πå[:\s]*(\d{2,3}[-\s]?\d{3}[-\s]?\d{4})',  # ‡πÇ‡∏ó‡∏£‡∏®‡∏±‡∏û‡∏ó‡πå: 02-123-4567
        r'‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏´‡∏ï‡∏∏[:\s]*(\d{4})',                 # ‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏´‡∏ï‡∏∏ 1557
        r'(?:‡πÇ‡∏ó‡∏£|‡πÄ‡∏ö‡∏≠‡∏£‡πå)\s*[:Ôºö]?\s*(\d{10})',     # ‡πÇ‡∏ó‡∏£: 0987654321
    ]

    for pattern in patterns:
        match = re.search(pattern, text)
        if match:
            phone = match.group(1).replace('-', '').replace(' ', '')
            return phone

    return None


def process_search_result(line_bot_api, event, user_id, policies, use_push=False):
    """
    ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡∏Å‡∏≤‡∏£‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ ‡∏™‡πà‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ï‡∏≠‡∏ö‡∏Å‡∏•‡∏±‡∏ö ‡πÅ‡∏•‡∏∞‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï state
    """
    if not policies:
        msg = TextMessage(text="‚ùå ‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Å‡∏£‡∏°‡∏ò‡∏£‡∏£‡∏°‡πå\n\n‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏≠‡∏µ‡∏Å‡∏Ñ‡∏£‡∏±‡πâ‡∏á ‡∏´‡∏£‡∏∑‡∏≠‡∏ï‡∏¥‡∏î‡∏ï‡πà‡∏≠‡πÄ‡∏à‡πâ‡∏≤‡∏´‡∏ô‡πâ‡∏≤‡∏ó‡∏µ‡πà")
